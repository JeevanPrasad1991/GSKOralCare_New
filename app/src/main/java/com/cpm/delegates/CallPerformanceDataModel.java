package com.cpm.delegates;


public class CallPerformanceDataModel {
	
	String product;
	String mtd ;
	String product_target ;
	String achievement;

	public String getProduct() {
		return product;
	}
	public void setProduct(String product) {
		this.product = product;
	}
	public String getMtd() {
		return mtd;
	}
	public void setMtd(String mtd) {
		this.mtd = mtd;
	}
	public String getProduct_target() {
		return product_target;
	}
	public void setProduct_target(String product_target) {
		this.product_target = product_target;
	}
	public String getAchievement() {
		return achievement;
	}
	public void setAchievement(String achievement) {
		this.achievement = achievement;
	}
	
}
